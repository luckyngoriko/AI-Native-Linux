//! Rust ↔ proto translations for the gRPC `CapabilityRuntime` surface (T-033).
//!
//! The crate exposes two parallel type hierarchies:
//!
//! 1. **Rust value types** — [`crate::ActionLifecycleState`],
//!    [`crate::ActionDispatchKind`], [`crate::AdapterIOMode`],
//!    [`crate::AdapterStability`], [`crate::QueueClass`],
//!    [`crate::ExecutionFailureReason`], [`crate::RollbackOutcome`],
//!    [`crate::RuntimeErrorCode`], [`crate::AdapterManifest`],
//!    [`crate::RegisteredAdapter`], [`crate::ActionContext`], and the typed
//!    [`crate::RuntimeError`] taxonomy. These are spec-typed with closed
//!    enums per S10.1 §3.
//!
//! 2. **Proto message types** — generated by `tonic-build` from
//!    `proto/aios.runtime.v1alpha1.proto`. These are the wire shape per
//!    S10.1 §5.1.
//!
//! ## Status code mapping (S10.1 §3.8 / §18.x)
//!
//! [`runtime_error_to_status`] maps the typed [`crate::RuntimeError`] surface
//! onto `tonic::Status` per the closed `RuntimeErrorCode` table. The mapping
//! is the single source of truth for how the wire boundary reports runtime-
//! side failures — RPC-level transport failures (timeouts, cancellation,
//! connection drops) are surfaced by tonic itself with the canonical
//! `Code::Cancelled` / `Code::DeadlineExceeded` / `Code::Unavailable` codes
//! and never flow through this function.

// `tonic::Status` is intentionally a wide value type (carries headers,
// metadata, source error, ...). The `result_large_err` lint flags every
// `Result<_, Status>` return — boxing the error would only push allocations
// onto the hot path of a wire surface that is already designed for typed
// status propagation. Allow it at module scope; the alternative is to
// re-box everywhere the conversions/server modules return a Status, which
// fights the tonic API ergonomics.
//
// `missing_docs` is allowed at module scope because every enum mapping
// function below is a 1:1 Rust ↔ proto3 translation whose semantics are
// fully captured by its identifier (`foo_to_proto` / `foo_from_proto`).
// Documenting each individually would only echo the function name.
#![allow(clippy::result_large_err, missing_docs)]

