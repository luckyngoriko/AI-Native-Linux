/// Encode an [`aios_action::ActionEnvelope`] into the wire-bytes form carried
/// by `ValidateActionRequest.envelope_proto` / `ExecuteActionRequest.envelope_proto`.
///
/// Per task #26 in the .ai tracker, the in-tree wire format is the canonical
/// `serde_json` representation of the envelope. When `aios-action` ships its
/// own proto IDL, this function is the single touch point — the rest of the
/// service module references it by name. Pattern mirrors aios-policy T-023
/// verbatim.
///
/// # Errors
///
/// Returns `tonic::Status::internal` when serialisation fails. In practice
/// this cannot happen for a well-typed `ActionEnvelope` (the serde derive is
/// infallible over the value).
pub fn envelope_to_bytes(env: &aios_action::ActionEnvelope) -> Result<Vec<u8>, tonic::Status> {
    serde_json::to_vec(env)
        .map_err(|e| tonic::Status::internal(format!("envelope serialize failed: {e}")))
}

/// Decode the wire-bytes form into an [`aios_action::ActionEnvelope`].
///
/// # Errors
///
/// Returns `tonic::Status::invalid_argument` when the bytes are empty or
/// when `serde_json` cannot deserialize the payload into an `ActionEnvelope`.
pub fn envelope_from_bytes(bytes: &[u8]) -> Result<aios_action::ActionEnvelope, tonic::Status> {
    if bytes.is_empty() {
        return Err(tonic::Status::invalid_argument(
            "envelope_proto is empty (expected serialized aios.action.v1alpha1.ActionEnvelope)",
        ));
    }
    serde_json::from_slice(bytes).map_err(|e| {
        tonic::Status::invalid_argument(format!(
            "envelope_proto deserialize failed (JSON placeholder per task #26): {e}"
        ))
    })
}

