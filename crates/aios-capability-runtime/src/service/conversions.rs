/// Convert a Rust [`AdapterActionDeclaration`] into its wire form.
///
/// `target_schema` / `response_schema` are opaque JSON values on the Rust
/// side; on the wire we render them as serialised JSON strings so the proto
/// IDL does not have to track an open-ended schema vocabulary.
#[must_use]
pub fn adapter_action_declaration_to_proto(
    d: &AdapterActionDeclaration,
) -> proto::AdapterActionDeclaration {
    proto::AdapterActionDeclaration {
        action_kind: d.action_kind.clone(),
        target_schema_json: d.target_schema.to_string(),
        response_schema_json: d.response_schema.to_string(),
        rollback_strategy: d.rollback_strategy.clone(),
        timeout_seconds: d.timeout_seconds,
        template_string: d.template_string.clone().unwrap_or_default(),
        template_substitution_variables: d.template_substitution_variables.clone(),
    }
}

/// Convert the wire form back into a Rust [`AdapterActionDeclaration`].
///
/// # Errors
///
/// Returns [`RuntimeError::ManifestInvalid`] when `target_schema_json` or
/// `response_schema_json` is not a valid JSON value.
pub fn adapter_action_declaration_from_proto(
    p: &proto::AdapterActionDeclaration,
) -> Result<AdapterActionDeclaration, RuntimeError> {
    let target_schema: serde_json::Value = serde_json::from_str(&p.target_schema_json)
        .map_err(|e| RuntimeError::ManifestInvalid(format!("target_schema_json decode: {e}")))?;
    let response_schema: serde_json::Value = serde_json::from_str(&p.response_schema_json)
        .map_err(|e| RuntimeError::ManifestInvalid(format!("response_schema_json decode: {e}")))?;
    Ok(AdapterActionDeclaration {
        action_kind: p.action_kind.clone(),
        target_schema,
        response_schema,
        rollback_strategy: p.rollback_strategy.clone(),
        timeout_seconds: p.timeout_seconds,
        template_string: if p.template_string.is_empty() {
            None
        } else {
            Some(p.template_string.clone())
        },
        template_substitution_variables: p.template_substitution_variables.clone(),
    })
}

