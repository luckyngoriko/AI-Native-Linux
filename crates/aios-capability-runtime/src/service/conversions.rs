/// Convert a Rust [`AdapterManifest`] into its wire form.
#[must_use]
pub fn adapter_manifest_to_proto(m: &AdapterManifest) -> proto::AdapterManifest {
    proto::AdapterManifest {
        adapter_id: m.adapter_id.clone(),
        adapter_version: m.adapter_version.clone(),
        vendor: m.vendor.clone(),
        name: m.name.clone(),
        declared_stability: i32::from(stability_to_proto(m.declared_stability)),
        io_mode: i32::from(io_mode_to_proto(m.io_mode)),
        dispatch_kind: i32::from(dispatch_kind_to_proto(m.dispatch_kind)),
        declared_actions: m
            .declared_actions
            .iter()
            .map(adapter_action_declaration_to_proto)
            .collect(),
        declared_invariants_supported: m.declared_invariants_supported.clone(),
        default_adapter_timeout_seconds: m.default_adapter_timeout_seconds,
        default_sandbox_profile_id: m.default_sandbox_profile_id.clone(),
        adapter_signature: m.adapter_signature.clone(),
        signing_key_id: m.signing_key_id.clone(),
        manifest_created_at: Some(datetime_to_proto(m.manifest_created_at)),
        manifest_expires_at: Some(datetime_to_proto(m.manifest_expires_at)),
    }
}

/// Convert the wire form back into a Rust [`AdapterManifest`].
///
/// # Errors
///
/// Returns [`RuntimeError::ManifestInvalid`] when any required nested field
/// (closed enum, declared action JSON schema, ...) fails to decode.
pub fn adapter_manifest_from_proto(
    p: &proto::AdapterManifest,
) -> Result<AdapterManifest, RuntimeError> {
    let declared_stability = stability_from_proto(
        proto::AdapterStability::try_from(p.declared_stability).map_err(|_| {
            RuntimeError::ManifestInvalid(format!("declared_stability={}", p.declared_stability))
        })?,
    )
    .ok_or_else(|| RuntimeError::ManifestInvalid("declared_stability=UNSPECIFIED".into()))?;
    let io_mode = io_mode_from_proto(
        proto::AdapterIoMode::try_from(p.io_mode)
            .map_err(|_| RuntimeError::ManifestInvalid(format!("io_mode={}", p.io_mode)))?,
    )
    .ok_or_else(|| RuntimeError::ManifestInvalid("io_mode=UNSPECIFIED".into()))?;
    let dispatch_kind = dispatch_kind_from_proto(
        proto::ActionDispatchKind::try_from(p.dispatch_kind).map_err(|_| {
            RuntimeError::ManifestInvalid(format!("dispatch_kind={}", p.dispatch_kind))
        })?,
    )
    .ok_or_else(|| RuntimeError::ManifestInvalid("dispatch_kind=UNSPECIFIED".into()))?;
    let declared_actions = p
        .declared_actions
        .iter()
        .map(adapter_action_declaration_from_proto)
        .collect::<Result<Vec<_>, _>>()?;
    let manifest_created_at = p
        .manifest_created_at
        .map_or_else(unix_epoch, datetime_from_proto);
    let manifest_expires_at = p
        .manifest_expires_at
        .map_or_else(unix_epoch, datetime_from_proto);
    Ok(AdapterManifest {
        adapter_id: p.adapter_id.clone(),
        adapter_version: p.adapter_version.clone(),
        vendor: p.vendor.clone(),
        name: p.name.clone(),
        declared_stability,
        io_mode,
        dispatch_kind,
        declared_actions,
        declared_invariants_supported: p.declared_invariants_supported.clone(),
        default_adapter_timeout_seconds: p.default_adapter_timeout_seconds,
        default_sandbox_profile_id: p.default_sandbox_profile_id.clone(),
        adapter_signature: p.adapter_signature.clone(),
        signing_key_id: p.signing_key_id.clone(),
        manifest_created_at,
        manifest_expires_at,
    })
}

// ---------------------------------------------------------------------------
// RegisteredAdapter <-> proto
