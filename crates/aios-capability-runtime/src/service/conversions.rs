#[must_use]
pub fn action_context_to_proto(c: &ActionContext) -> proto::ActionContext {
    proto::ActionContext {
        action_id: c.action_id.to_string(),
        status: i32::from(lifecycle_state_to_proto(c.status)),
        dispatch_kind: i32::from(dispatch_kind_to_proto(c.dispatch_kind)),
        queue_class: i32::from(queue_class_to_proto(c.queue_class)),
        created_at: Some(datetime_to_proto(c.created_at)),
        last_updated_at: Some(datetime_to_proto(c.last_updated_at)),
        // `0` (`_UNSPECIFIED`) for None is the proto3 convention; the
        // receiver re-projects to `Option<...>` via the proto enum's
        // `Unspecified` arm.
        error: c.error.map_or(0, |r| i32::from(failure_reason_to_proto(r))),
        rollback_outcome: c
            .rollback_outcome
            .map_or(0, |o| i32::from(rollback_outcome_to_proto(o))),
        evidence_chain: c.evidence_chain.clone(),
    }
}

/// # Errors
///
/// Returns [`RuntimeError::Internal`] when the `action_id` string fails to
/// parse back into an `aios_action::ActionId`.
pub fn action_context_from_proto(p: &proto::ActionContext) -> Result<ActionContext, RuntimeError> {
    let action_id = aios_action::ActionId::parse(&p.action_id)
        .map_err(|e| RuntimeError::Internal(format!("action_id parse: {e}")))?;
    let status = lifecycle_state_from_proto(
        proto::ActionLifecycleState::try_from(p.status)
            .map_err(|_| RuntimeError::Internal(format!("status={}", p.status)))?,
    )
    .ok_or_else(|| RuntimeError::Internal("status=UNSPECIFIED".into()))?;
    let dispatch_kind = dispatch_kind_from_proto(
        proto::ActionDispatchKind::try_from(p.dispatch_kind)
            .map_err(|_| RuntimeError::Internal(format!("dispatch_kind={}", p.dispatch_kind)))?,
    )
    .ok_or_else(|| RuntimeError::Internal("dispatch_kind=UNSPECIFIED".into()))?;
    let queue_class = queue_class_from_proto(
        proto::QueueClass::try_from(p.queue_class)
            .map_err(|_| RuntimeError::Internal(format!("queue_class={}", p.queue_class)))?,
    )
    .ok_or_else(|| RuntimeError::Internal("queue_class=UNSPECIFIED".into()))?;
    let created_at = p.created_at.map_or_else(unix_epoch, datetime_from_proto);
    let last_updated_at = p
        .last_updated_at
        .map_or_else(unix_epoch, datetime_from_proto);
    let error = proto::ExecutionFailureReason::try_from(p.error)
        .ok()
        .and_then(failure_reason_from_proto);
    let rollback_outcome = proto::RollbackOutcome::try_from(p.rollback_outcome)
        .ok()
        .and_then(rollback_outcome_from_proto);
    Ok(ActionContext {
        action_id,
        status,
        dispatch_kind,
        queue_class,
        created_at,
        last_updated_at,
        error,
        rollback_outcome,
        evidence_chain: p.evidence_chain.clone(),
    })
}

