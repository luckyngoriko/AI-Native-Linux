#[must_use]
pub fn registered_adapter_to_proto(r: &RegisteredAdapter) -> proto::RegisteredAdapter {
    proto::RegisteredAdapter {
        manifest: Some(adapter_manifest_to_proto(&r.manifest)),
        registered_at: Some(datetime_to_proto(r.registered_at)),
    }
}

/// # Errors
///
/// Returns [`RuntimeError::ManifestInvalid`] when the embedded
/// [`AdapterManifest`] is missing or fails decoding.
pub fn registered_adapter_from_proto(
    p: &proto::RegisteredAdapter,
) -> Result<RegisteredAdapter, RuntimeError> {
    let manifest_proto = p.manifest.as_ref().ok_or_else(|| {
        RuntimeError::ManifestInvalid("registered_adapter.manifest missing".into())
    })?;
    let manifest = adapter_manifest_from_proto(manifest_proto)?;
    let registered_at = p.registered_at.map_or_else(unix_epoch, datetime_from_proto);
    Ok(RegisteredAdapter {
        manifest,
        registered_at,
    })
}

