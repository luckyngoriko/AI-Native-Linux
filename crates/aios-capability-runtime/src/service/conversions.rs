/// Convert a `chrono::DateTime<Utc>` into the prost well-known `Timestamp`.
///
/// Lossless for every instant chrono can represent (`i64` seconds since the
/// Unix epoch + nanos).
#[must_use]
pub fn datetime_to_proto(dt: DateTime<Utc>) -> Timestamp {
    Timestamp {
        seconds: dt.timestamp(),
        // `timestamp_subsec_nanos` returns a `u32` in `0..1_000_000_000`,
        // which fits into the proto `i32` field by construction.
        nanos: i32::try_from(dt.timestamp_subsec_nanos()).unwrap_or(0),
    }
}

/// Convert the prost well-known `Timestamp` back into `chrono::DateTime<Utc>`.
///
/// Returns the Unix epoch when the proto value is out of range.
#[must_use]
pub fn datetime_from_proto(ts: Timestamp) -> DateTime<Utc> {
    Utc.timestamp_opt(ts.seconds, u32::try_from(ts.nanos).unwrap_or(0))
        .single()
        .unwrap_or_else(unix_epoch)
}

/// Return the Unix epoch (`1970-01-01T00:00:00Z`) as a `DateTime<Utc>`.
///
/// Used as the safe fallback when an absent / out-of-range proto timestamp
/// must still produce a typed Rust value.
#[must_use]
fn unix_epoch() -> DateTime<Utc> {
    Utc.timestamp_opt(0, 0).single().unwrap_or_default()
}

