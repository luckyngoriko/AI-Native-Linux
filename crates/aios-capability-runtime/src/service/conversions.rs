// RuntimeError -> tonic::Status (status mapping table)
// ---------------------------------------------------------------------------

/// Map a typed [`RuntimeError`] onto a `tonic::Status` per the closed
/// [`RuntimeErrorCode`] table.
///
/// The mapping table:
///
/// | `RuntimeError` variant              | `tonic::Code`           | `RuntimeErrorCode`              |
/// | ----------------------------------- | ----------------------- | ------------------------------- |
/// | `ActionNotFound`                    | `NotFound`              | `RuntimeInternal`               |
/// | `InvalidTransition { .. }`          | `FailedPrecondition`    | `LifecycleIllegalTransition`    |
/// | `AdapterUnknown(_)`                 | `NotFound`              | `UnknownAdapter`                |
/// | `AdapterSignatureInvalid`           | `PermissionDenied`      | `ManifestSignatureInvalid`      |
/// | `AdapterUnknownAuthority(_)`        | `PermissionDenied`      | `ManifestSignatureInvalid`      |
/// | `AdapterAlreadyRegistered(_)`       | `AlreadyExists`         | `RuntimeInternal`               |
/// | `ManifestInvalid(_)`                | `InvalidArgument`       | `ManifestSignatureInvalid`      |
/// | `QueueFull(_)`                      | `ResourceExhausted`     | `QueueBackpressureRejected`     |
/// | `RateLimited(_)`                    | `ResourceExhausted`     | `QueueBackpressureRejected`     |
/// | `PolicyEvalFailed(_)`               | `FailedPrecondition`    | `PolicyDecisionUnavailable`     |
/// | `EvidenceEmitFailed(_)`             | `Internal`              | `EvidenceLogUnavailable`        |
/// | `Internal(_)`                       | `Internal`              | `RuntimeInternal`               |
///
/// Code-only API: the wire layer consumes the `Status` directly; the
/// `RuntimeErrorCode` projection is documented here for parity with the
/// closed §3.8 enum and used by tests.
#[must_use]
pub fn runtime_error_to_status(e: &RuntimeError) -> tonic::Status {
    match e {
        RuntimeError::ActionNotFound(id) => {
            tonic::Status::not_found(format!("action not found: {id}"))
        }
        RuntimeError::InvalidTransition { from, to } => tonic::Status::failed_precondition(
            format!("illegal lifecycle transition: {from:?} -> {to:?}"),
        ),
        RuntimeError::AdapterUnknown(id) => {
            tonic::Status::not_found(format!("unknown adapter: {id}"))
        }
        RuntimeError::AdapterSignatureInvalid => {
            tonic::Status::permission_denied("adapter manifest signature invalid")
        }
        RuntimeError::AdapterUnknownAuthority(id) => tonic::Status::permission_denied(format!(
            "adapter manifest unknown signing authority: {id}"
        )),
        RuntimeError::AdapterAlreadyRegistered(id) => {
            tonic::Status::already_exists(format!("adapter already registered: {id}"))
        }
        RuntimeError::ManifestInvalid(msg) => {
            tonic::Status::invalid_argument(format!("adapter manifest invalid: {msg}"))
        }
        RuntimeError::QueueFull(class) => {
            tonic::Status::resource_exhausted(format!("dispatch queue full for class: {class:?}"))
        }
        RuntimeError::RateLimited(subject) => {
            tonic::Status::resource_exhausted(format!("subject rate-limited: {subject}"))
        }
        RuntimeError::PolicyEvalFailed(msg) => {
            tonic::Status::failed_precondition(format!("policy evaluation failed: {msg}"))
        }
        RuntimeError::EvidenceEmitFailed(msg) => {
            tonic::Status::internal(format!("evidence emission failed: {msg}"))
        }
        RuntimeError::Internal(msg) => {
            tonic::Status::internal(format!("runtime internal error: {msg}"))
        }
    }
}

/// Projection of a typed [`RuntimeError`] onto its [`RuntimeErrorCode`] wire
/// counterpart. Used by tests that assert the mapping table; the wire layer
/// itself uses [`runtime_error_to_status`].
#[must_use]
pub const fn runtime_error_to_code(e: &RuntimeError) -> RuntimeErrorCode {
    match e {
        RuntimeError::ActionNotFound(_)
        | RuntimeError::AdapterAlreadyRegistered(_)
        | RuntimeError::Internal(_) => RuntimeErrorCode::RuntimeInternal,
        RuntimeError::InvalidTransition { .. } => RuntimeErrorCode::LifecycleIllegalTransition,
        RuntimeError::AdapterUnknown(_) => RuntimeErrorCode::UnknownAdapter,
        RuntimeError::AdapterSignatureInvalid
        | RuntimeError::AdapterUnknownAuthority(_)
        | RuntimeError::ManifestInvalid(_) => RuntimeErrorCode::ManifestSignatureInvalid,
        RuntimeError::QueueFull(_) | RuntimeError::RateLimited(_) => {
            RuntimeErrorCode::QueueBackpressureRejected
        }
        RuntimeError::PolicyEvalFailed(_) => RuntimeErrorCode::PolicyDecisionUnavailable,
        RuntimeError::EvidenceEmitFailed(_) => RuntimeErrorCode::EvidenceLogUnavailable,
    }
}

